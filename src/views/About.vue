    How does this tool work?

Risk Score:
COVID-19, aka coronavirus, is a new virus that the public health and medical community is still learning about. Our team reviewed public comments made by infectious disease experts and, when available, published scientific literature, to assess the potential risk of getting and spreading COVID-19 posed by doing common, everyday things. Two independent reviewers rated the overall risk of each activity and for specific seven age groups. These ratings were independently reviewed by a panel of experts and revised based on their comments and feedback. These ratings reflect what we know about risk associated with doing any activity and are reviewed each week to reflect changing research on COVID-19. 

